public class Capybara implements Animal {
    
    public void walk() {
        System.out.println("Capybara is walking");
    }
    public void run() {
        System.out.println("Capybara is running");
    }
    public void jump() {
        System.out.println("Capybara is jumping");
    }
    public void swim() {
        System.out.println("Capybara is swimming");
    }
}
